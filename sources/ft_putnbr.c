#include "../includes/ft_printf.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n >= 0 && n < 10)
		ft_putchar((n + 48));
	if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		ft_putnbr((n * -1));
	}
	if (n >= 10)
	{
		ft_putnbr((n / 10));
		ft_putnbr((n % 10));
	}
}
