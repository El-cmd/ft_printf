#include "../includes/ft_printf.h"

void	ft_putnbr_u(long int n)
{
	if (n >= 0 && n < 10)
		ft_putchar((n + 48));
	if (n >= 10)
	{
		ft_putnbr_u((n / 10));
		ft_putnbr_u((n % 10));
	}
}
